document.addEventListener('DOMContentLoaded', function () {

    /* Carousel */
    const slides = document.querySelectorAll('.rbs-slide');
    const dots   = document.querySelectorAll('.rbs-dot');
    let cur = 0;
    function goTo(n) {
        slides.forEach((s,i) => s.style.opacity = i===n ? '1':'0');
        dots.forEach((d,i)   => d.style.opacity = i===n ? '1':'.4');
        cur = n;
    }
    dots.forEach((d,i) => d.addEventListener('click', () => goTo(i)));
    setInterval(() => goTo((cur+1) % slides.length), 6000);

